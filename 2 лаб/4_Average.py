def calculate_average():
    total = 0         # Сумма всех положительных чисел
    count = 0         # Счетчик положительных чисел

    while True:
        try:
            number = int(input("Введите положительное целое число (0 для выхода): "))
            
            if number == 0:  # Проверяем на выход
                break
            
            if number < 0:  # Игнорируем отрицательные числа
                print("Пожалуйста, введите положительное целое число.")
                continue
            
            total += number  # Накапливаем сумму
            count += 1      # Увеличиваем счетчик

        except ValueError:  # Обработка некорректного ввода
            print("Это не целое число. Попробуйте снова.")

    # Проверка, чтобы избежать деления на ноль
    if count > 0:
        average = total / count
        print(f"Среднее арифметическое: {average:.2f}")
    else:
        print("Не было введено ни одного положительного числа.")

calculate_average()
